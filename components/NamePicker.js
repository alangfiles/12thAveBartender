export function randomDrinkName(seed) {
  let rng = Math.random;

  const getAdjective = () => adjectives[Math.floor(rng() * adjectives.length)];
  const getNoun = () => nouns[Math.floor(rng() * nouns.length)];

  const getWord = () =>
    `${getAdjective()}${rng() < 0.25 ? ` ${getAdjective()}` : ""} ${getNoun()}`;

  return getWord();
}

